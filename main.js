function initReveal() {
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.18 }
  );

  document.querySelectorAll(".reveal").forEach((node) => observer.observe(node));
}

function initMemeCarousel() {
  const getCards = () => Array.from(memeGrid.querySelectorAll(".meme-card"));

  const updateActiveDot = () => {
    const cards = getCards();
    if (!cards.length) return;

    const gridRect = memeGrid.getBoundingClientRect();
    let activeIndex = 0;
    let minDistance = Number.POSITIVE_INFINITY;

    cards.forEach((card, index) => {
      const distance = Math.abs(card.getBoundingClientRect().left - gridRect.left);
      if (distance < minDistance) {
        minDistance = distance;
        activeIndex = index;
      }
    });

    memeDots.querySelectorAll(".meme-dot").forEach((dot, index) => {
      dot.classList.toggle("is-active", index === activeIndex);
    });
  };

  const scrollToIndex = (index) => {
    const cards = getCards();
    const card = cards[index];
    if (!card) return;
    card.scrollIntoView({ behavior: "smooth", inline: "start", block: "nearest" });
  };

  memePrev.addEventListener("click", () => {
    const current = Array.from(memeDots.querySelectorAll(".meme-dot")).findIndex((dot) =>
      dot.classList.contains("is-active")
    );
    const nextIndex = current <= 0 ? getCards().length - 1 : current - 1;
    scrollToIndex(nextIndex);
  });

  memeNext.addEventListener("click", () => {
    const current = Array.from(memeDots.querySelectorAll(".meme-dot")).findIndex((dot) =>
      dot.classList.contains("is-active")
    );
    const nextIndex = current >= getCards().length - 1 ? 0 : current + 1;
    scrollToIndex(nextIndex);
  });

  memeDots.addEventListener("click", (event) => {
    const target = event.target;
    if (!(target instanceof HTMLButtonElement)) return;
    const index = Number(target.dataset.index);
    scrollToIndex(index);
  });

  let scrollTimer;
  memeGrid.addEventListener("scroll", () => {
    window.clearTimeout(scrollTimer);
    scrollTimer = window.setTimeout(updateActiveDot, 80);
  });

  updateActiveDot();
}

function resizeConfetti() {
  const dpr = Math.min(window.devicePixelRatio || 1, 2);
  confettiWidth = window.innerWidth;
  confettiHeight = window.innerHeight;
  confettiCanvas.width = confettiWidth * dpr;
  confettiCanvas.height = confettiHeight * dpr;
  confettiCanvas.style.width = `${confettiWidth}px`;
  confettiCanvas.style.height = `${confettiHeight}px`;
  confettiContext.setTransform(dpr, 0, 0, dpr, 0, 0);
}

function spawnConfetti(x, y, amount = 24) {
  const colors = ["#ff7ea8", "#ffa6a6", "#ffd37a", "#9ed9ff", "#c5b3ff", "#ffffff"];

  for (let index = 0; index < amount; index += 1) {
    confettiParticles.push({
      x,
      y,
      size: Math.random() * 10 + 5,
      color: colors[Math.floor(Math.random() * colors.length)],
      angle: Math.random() * Math.PI * 2,
      speed: Math.random() * 4 + 2,
      rotate: Math.random() * 0.2 - 0.1,
      rotation: Math.random() * Math.PI,
      alpha: 1,
      drift: Math.random() * 0.8 - 0.4,
      gravity: Math.random() * 0.18 + 0.08,
    });
  }
}

function animateConfetti() {
  confettiContext.clearRect(0, 0, confettiWidth, confettiHeight);

  confettiParticles.forEach((particle, index) => {
    particle.x += Math.cos(particle.angle) * particle.speed + particle.drift;
    particle.y += Math.sin(particle.angle) * particle.speed + particle.gravity;
    particle.rotation += particle.rotate;
    particle.alpha -= 0.008;

    confettiContext.save();
    confettiContext.globalAlpha = Math.max(particle.alpha, 0);
    confettiContext.translate(particle.x, particle.y);
    confettiContext.rotate(particle.rotation);
    confettiContext.fillStyle = particle.color;
    confettiContext.fillRect(-particle.size / 2, -particle.size / 3, particle.size, particle.size * 0.66);
    confettiContext.restore();

    if (particle.alpha <= 0 || particle.y > confettiHeight + 40) {
      confettiParticles.splice(index, 1);
    }
  });

  requestAnimationFrame(animateConfetti);
}

function noteFrequency(note) {
  const table = {
    C4: 261.63,
    D4: 293.66,
    E4: 329.63,
    G4: 392,
    A4: 440,
    C5: 523.25,
  };

  return table[note];
}

function ensureAudio() {
  if (audioContext) return;

  const AudioCtx = window.AudioContext || window.webkitAudioContext;
  audioContext = new AudioCtx();
  masterGain = audioContext.createGain();
  masterGain.gain.value = 0.07;
  masterGain.connect(audioContext.destination);
}

function playTone(frequency, startTime, duration) {
  const oscillator = audioContext.createOscillator();
  const gain = audioContext.createGain();

  oscillator.type = "triangle";
  oscillator.frequency.value = frequency;
  gain.gain.setValueAtTime(0.0001, startTime);
  gain.gain.exponentialRampToValueAtTime(0.18, startTime + 0.02);
  gain.gain.exponentialRampToValueAtTime(0.0001, startTime + duration);

  oscillator.connect(gain);
  gain.connect(masterGain);
  oscillator.start(startTime);
  oscillator.stop(startTime + duration + 0.03);
}

function startMusic() {
  ensureAudio();

  if (audioContext.state === "suspended") {
    audioContext.resume();
  }

  if (melodyTimer) return;

  const melody = [
    { note: "C4", duration: 0.24 },
    { note: "E4", duration: 0.24 },
    { note: "G4", duration: 0.24 },
    { note: "A4", duration: 0.3 },
    { note: "G4", duration: 0.22 },
    { note: "E4", duration: 0.22 },
    { note: "D4", duration: 0.24 },
    { note: "C4", duration: 0.4 },
    { note: "G4", duration: 0.22 },
    { note: "A4", duration: 0.22 },
    { note: "C5", duration: 0.38 },
    { note: "G4", duration: 0.28 },
  ];

  const scheduleMelody = () => {
    let cursor = audioContext.currentTime + 0.04;
    melody.forEach((item) => {
      playTone(noteFrequency(item.note), cursor, item.duration);
      cursor += item.duration + 0.06;
    });
  };

  scheduleMelody();
  melodyTimer = window.setInterval(scheduleMelody, 5200);
}

function startExperience() {
  document.body.classList.remove("is-locked");
  document.body.classList.add("is-started");
  startMusic();
  spawnConfetti(window.innerWidth * 0.5, window.innerHeight * 0.24, 140);
}

function initScratchCard() {
  const ctx = scratchCanvas.getContext("2d", { willReadFrequently: true });
  const state = {
    scratched: false,
    revealed: false,
  };

  const drawCover = () => {
    const dpr = Math.min(window.devicePixelRatio || 1, 2);
    const rect = scratchReveal.getBoundingClientRect();

    scratchCanvas.width = rect.width * dpr;
    scratchCanvas.height = rect.height * dpr;
    scratchCanvas.style.width = `${rect.width}px`;
    scratchCanvas.style.height = `${rect.height}px`;
    ctx.setTransform(dpr, 0, 0, dpr, 0, 0);
    ctx.globalCompositeOperation = "source-over";
    ctx.clearRect(0, 0, rect.width, rect.height);

    const gradient = ctx.createLinearGradient(0, 0, rect.width, rect.height);
    gradient.addColorStop(0, "#ff86a8");
    gradient.addColorStop(0.5, "#ffc09e");
    gradient.addColorStop(1, "#8cd8ff");
    ctx.fillStyle = gradient;
    ctx.fillRect(0, 0, rect.width, rect.height);

    ctx.fillStyle = "rgba(255,255,255,0.18)";
    for (let i = 0; i < 80; i += 1) {
      const x = Math.random() * rect.width;
      const y = Math.random() * rect.height;
      const r = Math.random() * 2.8 + 1;
      ctx.beginPath();
      ctx.arc(x, y, r, 0, Math.PI * 2);
      ctx.fill();
    }

    ctx.fillStyle = "rgba(255,255,255,0.95)";
    ctx.font = '800 20px "Be Vietnam Pro"';
    ctx.textAlign = "center";
    ctx.fillText("Cào để mở quà cho chị đẹp", rect.width / 2, rect.height / 2 - 4);
    ctx.font = '700 14px "Be Vietnam Pro"';
    ctx.fillText("Kéo ngón tay hoặc chuột trên vùng màu này", rect.width / 2, rect.height / 2 + 24);

    state.revealed = false;
  };

  const scratchAt = (clientX, clientY) => {
    const rect = scratchCanvas.getBoundingClientRect();
    const x = clientX - rect.left;
    const y = clientY - rect.top;
    ctx.globalCompositeOperation = "destination-out";
    ctx.beginPath();
    ctx.arc(x, y, 24, 0, Math.PI * 2);
    ctx.fill();
  };

  const revealIfNeeded = () => {
    if (state.revealed) return;

    const sample = ctx.getImageData(0, 0, scratchCanvas.width, scratchCanvas.height).data;
    let cleared = 0;

    for (let index = 3; index < sample.length; index += 4) {
      if (sample[index] === 0) cleared += 1;
    }

    const clearedRatio = cleared / (sample.length / 4);
    if (clearedRatio > 0.42) {
      state.revealed = true;
      scratchCanvas.style.transition = "opacity 0.3s ease";
      scratchCanvas.style.opacity = "0";
      spawnConfetti(window.innerWidth * 0.5, scratchReveal.getBoundingClientRect().top + 40, 56);
    }
  };

  const pointerMove = (event) => {
    if (!state.scratched) return;
    scratchAt(event.clientX, event.clientY);
  };

  scratchCanvas.addEventListener("pointerdown", (event) => {
    state.scratched = true;
    scratchCanvas.setPointerCapture(event.pointerId);
    scratchAt(event.clientX, event.clientY);
  });

  scratchCanvas.addEventListener("pointermove", pointerMove);

  const finishScratch = () => {
    if (!state.scratched) return;
    state.scratched = false;
    revealIfNeeded();
  };

  scratchCanvas.addEventListener("pointerup", finishScratch);
  scratchCanvas.addEventListener("pointercancel", finishScratch);
  scratchCanvas.addEventListener("pointerleave", finishScratch);

  scratchReset.addEventListener("click", () => {
    scratchCanvas.style.opacity = "1";
    drawCover();
  });

  window.addEventListener("resize", drawCover);
  drawCover();
}

